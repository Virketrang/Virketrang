export default abstract class GlobalValues {
    public static get inherit() {
        return 'inherit';
    }

    public static get initial() {
        return 'initial';
    }

    public static get revert() {
        return 'revert';
    }

    public static get revertLayer() {
        return 'revert-layer';
    }

    public static get unset() {
        return 'unset';
    }
}
